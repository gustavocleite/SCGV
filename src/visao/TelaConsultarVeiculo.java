    //Metodo privado pra preencher combobox com Veiculos
    private void preencherComboBoxVeiculos() {
        try {
            listarVeiculos = consultarVeiculo.listagemDeVeiculos();
            // Ordenar a lista de veículos em ordem crescente com base no ID
            Collections.sort(listarVeiculos, (Veiculo v1, Veiculo v2) -> Integer.compare(v1.getId(), v2.getId()));

            for (Veiculo i : listarVeiculos) {
                jComboBox_IDveiculos.addItem(i.getId() + " - " + i.getPlaca());
            }

            if (!listarVeiculos.isEmpty()) {
                jComboBox_IDveiculos.setSelectedIndex(0);
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }

