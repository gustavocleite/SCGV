    private void jButton_ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ConsultarActionPerformed

        String veiculoSelecionado = (String) jComboBox_IDveiculos.getSelectedItem();

        if (veiculoSelecionado.equals("Escolha o veiculo")) {
            JOptionPane.showMessageDialog(this, "Por favor, escolha um veículo válido!");
            return;
        }
        try {
            int idVeiculo = Integer.parseInt(veiculoSelecionado.split(" - ")[0]);
            Veiculo veiculo = consultarVeiculo.consultar(idVeiculo);
            //Busca e lista Proprietario
            String[] colunas = {"ID", "PROPRIETÁRIO", "PLACA", "MARCA", "MODELO", "COMBUSTIVEL", "KM ATUAL"};
            DefaultTableModel modeloTabela = new DefaultTableModel(colunas, 0);
            Object[] linha = {veiculo.getId(), veiculo.getProprietario(), veiculo.getPlaca(),
                veiculo.getMarca(), veiculo.getModelo(), veiculo.getTipoCombustivel(), veiculo.getKmAtual()};
            modeloTabela.addRow(linha);

            jTable_Saida.setModel(modeloTabela);// Saida de dados

            //Busca imagem do proprietario
            setImagemModelo(veiculo.getFotoVeiculo().getPath());
            //Busca imagem da cnh
            setImagemMarca(veiculo.getFotoMarca().getPath());
            limparComboBoxVeiculo();
            preencherComboBoxVeiculos();

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButton_ConsultarActionPerformed

